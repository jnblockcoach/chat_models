print(f"基础知识对: {len(items)}")

# Generate questions for each pair
for p, a in items:
    pos = p.find(a)
    if pos < 0: continue
    for q in [
        a + "是什么？",
        "什么是" + a,
        "请解释" + a,
        a[:8] + "的相关知识",
        a[:6] + "是哪个学科？",
        a[:5] + "指的是什么？",
        "关于" + a[:6],
    ]:
        add(p, q, a)

# Cross-match all pairs
all_paras = list(set(p for p, a in items))
all_answers = list(set(a for p, a in items))
added = 0
for p in all_paras:
    for a in all_answers:
        if len(a) >= 3 and a in p:
            for q in [
                a + "是什么？",
                "什么是" + a,
                a[:8] + "是什么",
            ]:
                add(p, q, a)
                added += 1

print(f"交叉匹配: +{added}")

# Generate paragraph variants: add 20 variations per unique paragraph
para_variants = []
for p in all_paras:
    for suffix in ["这是教材中的重要知识点。","这个知识经常考。","学生需要掌握。","考试中常出现。"]:
        np = p + suffix
        para_variants.append(np)

for p in para_variants:
    for a in all_answers:
        if len(a) >= 3 and a in p:
            add(p, a + "是什么？", a)

print(f"段落变体: +{len(results) - len(items)*8}")

