def generate_qa(subject, subtopic, para, item):
    """从段落生成问答对列表"""
    qa_pairs = []
    name = item[0]
    seen = set()
    
    # 根据问题类型生成不同的问题
    
    # 辅助:查找答案工具
    def add_qa(question, answer_text):
        nonlocal qa_pairs
        if not answer_text or not question:
            return
        if len(answer_text) < 2:
            return
        # 答案必须在段落中
        if answer_text not in para:
            # 尝试匹配部分内容
            for chunk_size in range(len(answer_text)-1, 5, -1):
                if answer_text[:chunk_size] in para:
                    answer_text = answer_text[:chunk_size]
                    break
            else:
                return
        key = (question[:15], answer_text[:15])
        if key in seen:
            return
        seen.add(key)
        
        start = para.find(answer_text)
        if start == -1:
            return
        end = start + len(answer_text)
        
        qa_pairs.append({
            "paragraph": para,
            "question": question,
            "answer": answer_text,
            "answer_start": start,
            "answer_end": end,
        })
    
    # 提取段落中的关键片段用于问题
    # 寻找段落中有意义的"知识点"片段
    # 策略:从name开始找,或从关键描述词开始找
    
    # 1. 是什么/什么是什么
    if name in para:
        idx = para.find(name)
        end = min(idx + 60, len(para))
        fact = para[idx:end]
        if len(fact) >= 5:
            q = random.choice([
                f"什么是{name}?",
                f"{name}是指什么?",
                f"请解释{name}的含义。",
                f"{name}是什么?",
            ])
            add_qa(q, fact)
    
    # 2. 为什么/有什么意义
    for kw in ["因为","所以","因此","原因","意义","影响","作用","功能","目的"]:
        if kw in para:
            idx = para.find(kw)
            start = max(0, idx - 10)
            end = min(idx + 55, len(para))
            fact = para[start:end]
            if len(fact) >= 8 and "意义" in fact or "作用" in fact or "影响" in fact:
                q = random.choice([
                    f"{name}有什么意义?",
                    f"{name}的作用是什么?",
                    f"{name}有什么影响?",
                    f"为什么{name}很重要?",
                ])
                add_qa(q, fact)
            break
    
    # 3. 什么特点/什么特征
    for kw in ["特点是","特征是","以","有","是","特点","特征"]:
        if kw in para and len(found := []) < 1:
            idx = para.find(name)
            if idx >= 0:
                end = min(idx + 65, len(para))
                fact = para[idx:end]
                if len(fact) >= 8:
                    q = random.choice([
                        f"{name}有什么特点?",
                        f"{name}有哪些主要特征?",
                        f"{name}指的是什么?",
                    ])
                    add_qa(q, fact)
                    break
    
    # 4. 谁/作者/人物
    for qtype in ["作者","诗人","谁","人物","提出者"]:
        if qtype in para:
            idx = para.find(qtype)
            end = min(idx + 30, len(para))
            if qtype == "作者":
                fact = para[idx:end]
                if len(fact) >= 5:
                    add_qa(f"{name}的作者是谁?", fact)
                break
            elif qtype == "提出者":
                fact = para[idx:end]
                if len(fact) >= 5:
                    add_qa(f"{name}的提出者是谁?", fact)
                break
            break
    
    # 5. 什么时候/时间
    years = re.findall(r'\d{3,4}年', para)
    for y in years[:2]:
        if y in para:
            idx = para.find(y)
            end = min(idx + 20, len(para))
            fact = para[max(0, idx-5):end]
            add_qa(f"{name}发生在什么时候?", fact)
    
    # 6. 多少/数量
    nums = re.findall(r'约?[0-9.]+[万千百亿公里米度个条座种]', para)
    for n in nums[:2]:
        if n in para:
            idx = para.find(n)
            end = min(idx + 25, len(para))
            fact = para[max(0, idx-5):end]
            add_qa(f"{name}的规模或数量是多少?", fact)
    
    # 7. 哪里/地点
    places = re.findall(r'[^\s，。；：]{2,6}(?:地区|地方|区域|盆地|平原|高原|山脉|河流|海洋|大洲|国家|城市|首都)', para)
    for p in places[:2]:
        if p in para:
            idx = para.find(p)
            end = min(idx + 25, len(para))
            fact = para[max(0, idx-5):end]
            add_qa(f"{name}位于哪里?", fact)
    
    # 8. 怎么做/如何
    for kw in ["方法","方式","步骤","过程","如何","怎样"]:
        if kw in para:
            idx = para.find(kw)
            end = min(idx + 50, len(para))
            fact = para[max(0, idx-5):end]
            if len(fact) >= 8:
                add_qa(f"如何进行{name}?", fact)
            break
    
    # 9. 属于哪一类/哪一种
    for kw in ["属于","分为","分类","类别","类型","种类"]:
        if kw in para:
            idx = para.find(kw)
            end = min(idx + 50, len(para))
            fact = para[max(0, idx-5):end]
            if len(fact) >= 8:
                add_qa(f"{name}属于哪一类?", fact)
            break
    
    # 10. 哪个朝代
    dynasties = re.findall(r'[秦汉唐宋元明清东西周春战魏晋南北隋五][朝代]', para)
    for d in dynasties[:1]:
        if d in para:
            add_qa(f"{name}发生在哪个朝代?", d)
    
    # 11. 例句(英语)
    if subject == "英语":
        for kw in ["例如","比如","如"]:
            if kw in para:
                idx = para.find(kw)
                end = min(idx + 55, len(para))
                fact = para[idx:end]
                if len(fact) >= 6:
                    add_qa(f"请举例说明{name}的用法。", fact)
                break
    
    # 12. 标志词(英语)
    for kw in ["标志","常与","常用","常见"]:
        if kw in para:
            idx = para.find(kw)
            end = min(idx + 50, len(para))
            fact = para[idx:end]
            if len(fact) >= 6:
                add_qa(f"{name}有哪些常见的标志词?", fact)
            break
    
    # 13. 公式(数学/科学)
    for kw in ["公式","定理","定律","原理","等于"]:
        if kw in para:
            idx = para.find(kw)
            end = min(idx + 55, len(para))
            fact = para[max(0, idx-5):end]
            if len(fact) >= 8:
                add_qa(f"{name}的公式或定律是什么?", fact)
            break
    
    # 14. 应用
    for kw in ["应用","用于","用途"]:
        if kw in para:
            idx = para.find(kw)
            end = min(idx + 50, len(para))
            fact = para[idx:end]
            if len(fact) >= 8:
                add_qa(f"{name}在实际中有哪些应用?", fact)
            break
    
    return qa_pairs


