def make_paragraph(subject, subtopic, item):
    """从知识点生成一个段落"""
    name, desc, extra = item[0], item[1], item[2] if len(item) > 2 else ""
    
    if subject == "语文":
        if subtopic in ("古诗文",):
            name1, author, lines, meaning = item[0], item[1], item[2], item[3]
            templates = [
                f"《{name1}》是{author}的代表诗作之一。诗中写道:{lines}这首诗{meaning}",
                f"唐代诗人{author}的《{name1}》是一首广为传诵的诗歌。全诗为:{lines}这首诗{meaning}",
                f"在{author}的《{name1}》中诗人写道:{lines}这首诗{meaning}",
            ]
        elif subtopic == "成语故事":
            templates = [
                f"成语'{name}'来源于一个古代故事。{desc}因此这个成语{extra}",
                f"'{name}'是一个常用的成语{extra}这个成语的故事是:{desc}",
                f"关于成语'{name}'有这样一个典故:{desc}这个成语{extra}",
            ]
        elif subtopic == "文言文":
            source, text, meaning = item[0], item[1], item[2]
            templates = [
                f"{source}中写道:{text}这句话的意思是{meaning}",
                f"在{source}中有一段著名的话:{text}对其的理解是{meaning}",
                f"出自{source}的名句:{text}这段话告诉我们的道理是{meaning}",
            ]
        elif subtopic == "现代文":
            title, author, passage, meaning = item[0], item[1], item[2], item[3]
            templates = [
                f"在{author}的散文《{title}》中作者写道:{passage}这段话{meaning}",
                f"《{title}》是{author}的名篇。文中写道:{passage}这段文字{meaning}",
                f"《{title}》中{author}写道:{passage}这段话{meaning}",
            ]
        else:
            templates = [f"关于{name}:{desc}{extra}"]
    elif subject == "数学":
        templates = [
            f"在数学中{name}是一个重要概念:{desc}{extra}",
            f"{name}的内容如下:{desc}{extra}",
            f"我们来学习{name}这个数学概念。{desc}{extra}",
        ]
    elif subject == "英语":
        templates = [
            f"在英语语法中{name}的用法是:{desc}{extra}",
            f"学习英语时{name}是一个重要知识点。{desc}{extra}",
            f"关于{name}:{desc}{extra}",
        ]
    elif subject == "科学":
        templates = [
            f"在{science_name(subtopic)}中{name}是指:{desc}{extra}",
            f"我们来了解{name}这个概念。{desc}{extra}",
            f"{name}是{science_name(subtopic)}中的重要概念:{desc}{extra}",
        ]
    elif subject == "历史":
        templates = [
            f"在历史上{name}是一件重大事件。{desc}{extra}",
            f"{name}发生在:{desc}{extra}",
            f"关于{name}:{desc}{extra}",
        ]
    elif subject == "地理":
        templates = [
            f"{name}是一个重要的地理知识点。{desc}{extra}",
            f"在地理学习中{name}需要我们掌握。{desc}{extra}",
            f"{name}的相关知识如下:{desc}{extra}",
        ]
    elif subject == "道德与法治":
        templates = [
            f"在道德与法治课程中{name}是一个重要内容。{desc}{extra}",
            f"我们来学习{name}这个概念。{desc}{extra}",
            f"{name}的含义是:{desc}{extra}",
        ]
    else:
        templates = [f"{name}:{desc}{extra}"]
    
    para = random.choice(templates)
    # 确保段落不要太长也不要太短
    if len(para) > 300:
        para = para[:297] + "..."
    elif len(para) < 50 and extra:
        para = para + extra[:100]
    elif len(para) < 50:
        para = para + name + desc[:50]
    return para, name


def science_name(st):
    """返回科学子分类的中文名"""
    mapping = {"物理":"物理学","化学":"化学","生物":"生物学"}
    return mapping.get(st, st)


